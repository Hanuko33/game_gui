do
     echo \"$s\", >> items_names.h
done
echo "};" >> items_names.h

echo -e "//autogenerated file, don't motify!\nenum category_id\n{" > categories_id.h
for e in `cat categories.txt`
do
     echo "CAT_$e," >> categories_id.h
done
