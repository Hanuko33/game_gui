echo "};" >> categories_id.h

echo -e "//autogenerated file, don't motify!\nconst char * categories_names[CAT_MAX]=\n{" > categories_names.h
for s in `cat categories.txt`
do
     echo \"$s\", >> categories_names.h
done
echo "};" >> categories_names.h

